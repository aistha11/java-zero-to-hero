
package np.edu.scst.variable;


public class Variable {
    // psvm
    public static void main(String[] args) {

        System.out.println("Variables in Java"); // sout
        // shift+F6 to run
        // datatype variable = literal;
        byte b = 13;
        short s = 14;
        int i = 15;
        // add "l" L letter after long literal value
        long l = 16l;
        // add "f" letter after float literal value
        float f = 17.04f;
        double d = 18.74;
        char c = 'a';
        boolean bool = true;
        String str = "I am a string";
        
        System.out.println(b);
        System.out.println(str);
    }
}
